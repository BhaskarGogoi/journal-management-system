				<h5>Call For Papers - Date: 03.01.2022</h5>
				<p align="justify">Original and high quality research papers are invited for publication in the <i>Journal of Politics</i> for its XXI volume to be published (both print and online) in the month of October, 2022. The last date for receipt of papers is August 31, 2021.</p>
				<h5>Guidelines for authors</h5>
				<p>
					<ul>
						<li align="justify">All manuscripts must be submitted online to the editor.</li>
						<li align="justify">The manuscript should not exceed 5000 words excluding abstract, key words, tables, notes and references.</li>
						<li align="justify">All manuscripts should be submitted through e-mail to the editor in MS Word format in 1.5 spaces with 12 point font size in Times New Roman.</li>
						<li align="justify">The cover page should contain (i) the title of the article (ii) the name(s) of the authors (iii) Professional affiliation and (iv) address for correspondence.</li>
						<li align="justify">The name(s) of author(s) should not appear in the first page or any of the pages of the manuscript to facilitate blind review.</li>
						<li align="justify">Each manuscript should contain an abstract of not more than 250 words.</li>
						<li align="justify">A set of 3 to 5 key words, arranged alphabetically may follow the abstract.</li>
						<li align="justify">Articles with two or more authors must have one corresponding author.</li>
						<li align="justify">While submitting articles authors must declare that the article has not been submitted to any other publisher for publication.</li>
					</ul>
				</p>
				<h5>Manuscript Formatting</h5>
				<p>
					<ul>
						<li align="justify">All articles should be typed on one side of the paper (preferably A4) and double-spaced throughout.</li>
						<li align="justify">Notes should be numbered serially (1,2,3…) and presented at the end of the article.</li>
						<li align="justify">British English be used throughout.</li>
						<li align="justify">Single quotes should be used throughout. Double quotes should only be used within single quotes. Italics should be used for non-English words.</li>
						<li align="justify">When a century is referred, the authors should use words, e.g., ‘Sixteenth century’ and when reference is being made to a decade, the authors should use, e.g., ‘1970s’.</li>
						<li align="justify">Tables and figures to be indicated by number (e.g., see Table 1).</li>
						<li align="justify">All books, articles, essays and documents referred to (including any referred to in the tables and maps) should be listed at the end of the article.</li>
					</ul>
				</p>
				<h5>References</h5>
				<p align="justify">Authors should follow the Citations and References (based on the Publication Manual of the American Psychological Association, 6th edition). Some examples are given below:</p>
				<h5>In text citations:</h5>
				<p align="justify">
					One work by one author: (Baruah, 2012, p. 56) or Baruah (2012) noted that the development of power sector in Tripura also led to a massive forced displacement of tribal people…’	
				</p>
				<p align="justify">One work by two authors: (Baylis &amp; Smith, 2002, pp. 110–140) or Baylis and Smith (2002) noted that…</p>
				<p align="justify">One work by three or more authors: (Borah, Deka &amp; Mukherjee, 2009) [first instance];</p>
				<p align="justify">Borah et al. (2009) [Second instance onwards].</p>
				<p align="justify">Groups or organizations or universities: (Dibrugarh University, 2016) or Dibrugarh University (2016).</p>
				<p align="justify">In case of authors with same surname include the initials in all the in-text citations e.g., (U. Mishra, 2006; T. Mishra, 2016).</p>
				<p align="justify">In case of works with no identified author cite the first few words of the title and the year, e.g., (‘The Fieldwork Report’, 2000) </p>

